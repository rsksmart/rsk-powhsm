#include <assert.h>
#include <stdio.h>
#include <string.h>

#include "sha256.h"

void do_test(uint8_t* midstate,
             uint8_t* txntail,
             size_t tailsize,
             uint8_t* expected) {
    SHA256_CTX ctx;
    sha256_init(&ctx);
    sha256_midstate(&ctx, midstate);
    sha256_update(&ctx, txntail, tailsize);

    uint8_t hash[SHA256_BLOCK_SIZE];
    sha256_final(&ctx, hash);

    printf("Computed hash: ");
    for (int i = 0; i < SHA256_BLOCK_SIZE; i++) {
        printf("%02x", hash[i]);
    }
    printf("\n");

    assert(memcmp(hash, expected, SHA256_BLOCK_SIZE) == 0);
}

