#include <assert.h>
#include <stdio.h>
#include <string.h>

#include "hmac_sha256.h"

void do_test(uint8_t* key,
             size_t key_size,
             uint8_t* data,
             size_t data_size,
             uint8_t* expected) {

    uint8_t hmac[HMAC_SHA256_SIZE];
    size_t res_size;

    res_size = hmac_sha256(key, key_size, data, data_size, hmac, sizeof(hmac));
    assert(res_size == HMAC_SHA256_SIZE);

    printf("Computed hmac-sha256: ");
    for (int i = 0; i < HMAC_SHA256_SIZE; i++) {
        printf("%02x", hmac[i]);
    }
    printf("\n");

    assert(memcmp(hmac, expected, HMAC_SHA256_SIZE) == 0);
}

